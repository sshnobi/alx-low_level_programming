/**
 * string_toupper - function to capitalize all letters in a string
 * @s: string to capitalize
 * Return: the capitalized string
 */

char *string_toupper(char *s)
{

	int i = 0;

	for (i = 0; s[i] != '\0'; i++)
	{
		if (s[i] >= 'a' && s[i] <= 'z')
			s[i] = s[i] - 'a' + 'A';
	}
	return (s);
}
